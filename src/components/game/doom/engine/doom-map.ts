export const PLAYER_START: Vector2D = { x: 2.5, y: 2.5 };

// 맵 타일 조회
export const getTile = (map: number[][], x: number, y: number): TileType => {
  const mapX = Math.floor(x);
  const mapY = Math.floor(y);
  if (mapX < 0 || mapY < 0 || mapY >= map.length || mapX >= map[0].length) {
    return TileType.WALL;
  }
  return map[mapY][mapX] as TileType;
};

// 이동 가능 여부 확인
export const isWalkable = (map: number[][], x: number, y: number): boolean => {
  return getTile(map, x, y) === TileType.EMPTY;
};
